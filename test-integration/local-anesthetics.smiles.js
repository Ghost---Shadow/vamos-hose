// Local Anesthetics and Topical Analgesics
// Pain relief through nerve block or topical application

// === LIDOCAINE ===
// Amide-type local anesthetic
export const lidocaine = 'CCN(CC)CC(=O)NC1=C(C)C=CC=C1C';

// === BUPIVACAINE ===
// Long-acting amide anesthetic
export const bupivacaine = 'CCCCN1CCCCC1C(=O)NC2=C(C)C=CC=C2C';

// === ROPIVACAINE ===
// Similar to bupivacaine but with n-propyl
export const ropivacaine = 'CCCN1CCCCC1C(=O)NC2=C(C)C=CC=C2C';

// === MEPIVACAINE ===
// Intermediate-duration amide anesthetic
export const mepivacaine = 'CN1CCCCC1C(=O)NC2=C(C)C=CC=C2C';

// === PRILOCAINE ===
// Amide anesthetic with lower toxicity
export const prilocaine = 'CCCNC(C)C(=O)NC1=CC=CC=C1C';

// === BENZOCAINE ===
// Ester-type topical anesthetic
export const benzocaine = 'CCOC(=O)C1=CC=C(C=C1)N';

// === PROCAINE ===
// Classic ester anesthetic (Novocain)
export const procaine = 'CCN(CC)CCOC(=O)C1=CC=C(C=C1)N';
